void Unit::deadinstelim()
{
  int count = 0;
  do
  {
    count = 0;
    auto inst = globalbb->getNext();
    for (; inst != globalbb; inst = inst->getNext())
    {
      auto op = inst->getDef();
      if(op!=nullptr&&op->getUse().empty())
      {
        auto next = inst->getNext();
        auto prev = inst->getPrev();
        (inst->getPrev())->setNext(next);
        (inst->getNext())->setPrev(prev);
        inst->setParent(nullptr);
        ++count;
        delete inst;
      }
    }
    for (auto &func : func_list)
        count += func->deadinstelim();
  }while(count!=0);
}

void Unit::insertFunc(Function *f)
{
    func_list.push_back(f);
}

void Unit::removeFunc(Function *func)
{
    func_list.erase(std::find(func_list.begin(), func_list.end(), func));
}

