Unit::~Unit()
{
    Instruction *inst;
    if(globalbb!=nullptr)
    {
        inst = globalbb->getNext();
        while (inst!=nullptr&&inst != inst->getNext())
        {
            Instruction *t;
            t = inst;
            inst = inst->getNext();
            delete t;
        }
    }
    auto delete_list = func_list;
    for(auto &func:delete_list)
        delete func;
        
}
